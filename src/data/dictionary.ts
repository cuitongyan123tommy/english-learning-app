export function translateWords(words: string[]): Record<string, string> {
  const result: Record<string, string> = {}
  for (const word of words) {
    const lower = word.toLowerCase()
    if (DICTIONARY[lower]) result[word] = DICTIONARY[lower]
  }
  return result
}
